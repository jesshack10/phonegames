export function getCategoryNames(lang) {
  return Object.keys(WORD_LISTS[lang] ?? WORD_LISTS.en)
}

export function getRandomWord(category, lang) {
  const lists = WORD_LISTS[lang] ?? WORD_LISTS.en
  const allLabel = lang === 'es' ? 'Todas' : 'All'
  const pool =
    category === allLabel
      ? Object.values(lists).flat()
      : lists[category] ?? Object.values(lists).flat()
  return pool[Math.floor(Math.random() * pool.length)]
}
